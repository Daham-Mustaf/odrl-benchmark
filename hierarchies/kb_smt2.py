"""
hierarchies/kb_smt2.py
SMT2 fragments for the GEO and supporting KBs.
Used by cat01_spatial.py to produce self-contained .smt2 files.

Design: each KB is encoded as:
  (1) Concept constant declarations
  (2) Distinct (UNA) assertion
  (3) Ground leq/2 facts
  (4) Ground disjoint/2 facts
  (5) ODRL core axioms (transitivity, downward closure, denotation rules)

Denotation rules are INLINED, not using in_denotation/3:
  isA / isPartOf  X  G  →  (leq X G)
  hasPart         X  G  →  (leq G X)
  eq              X  G  →  (= X G)
  neq             X  G  →  (not (= X G))

This keeps SMT2 files quantifier-light and Z3-friendly.
"""

