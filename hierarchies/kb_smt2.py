# ==========================================================================
# concept/1 predicate extension for tautology tests (ODRL060-062)
# In TPTP, concept(X) is grounded per-KB-concept + closure axiom.
# In SMT2, we replicate this as a declare-fun + membership + closure.
# ==========================================================================

def geo_concept_predicate_smt2(concepts: list[str]) -> str:
    """
    Generate SMT2 concept/1 predicate for a list of concept names.
    Adds: (declare-fun concept (C) Bool)
          (assert (concept X)) for each X
          Closure: ∀Y.concept(Y) ⇒ (Y=c1 ∨ ... ∨ Y=cn)
    """
    lines = []
    lines.append("; ─── concept/1 predicate (closed-world over declared concepts) ─────")
    lines.append("(declare-fun concept (C) Bool)")
    for c in concepts:
        lines.append(f"(assert (concept {c}))")
    # Closure (makes the quantifier useful)
    disjuncts = " ".join(f"(= Y {c})" for c in concepts)
    lines.append(f"(assert (forall ((Y C))")
    lines.append(f"    (=> (concept Y) (or {disjuncts}))))")
    return "\n".join(lines)


