# ==========================================================================
# ODRL Core axioms (SMT2 translation of ODRL000-0.ax relevant parts)
# Always included in self-contained .smt2 KB problems.
# ==========================================================================

ODRL_CORE_SMT2 = """\
; ─── ODRL Core axioms (ODRL000-0.ax) ─────────────────────────────────────
; Sort and predicate declarations
(declare-sort C 0)
(declare-fun leq  (C C) Bool)
(declare-fun disj (C C) Bool)

; leq: reflexive (asserted per-concept), transitive
(assert (forall ((x C)(y C)(z C))
    (=> (and (leq x y)(leq y z)) (leq x z))))

; disj: symmetric
(assert (forall ((x C)(y C))
    (=> (disj x y)(disj y x))))

; disj: irreflexive
(assert (forall ((x C))
    (not (disj x x))))

; disj: downward-closed
(assert (forall ((x C)(y C)(xp C)(yp C))
    (=> (and (disj x y)(leq xp x)(leq yp y))
        (disj xp yp))))

; disj/leq consistency: leq(x,y) → ¬disj(x,y)
(assert (forall ((x C)(y C))
    (=> (leq x y)(not (disj x y)))))

; leq: reflexivity (asserted per declared concept below)
"""

ODRL_REFLEXIVITY_SMT2_TEMPLATE = """\
; leq reflexivity for all concepts
{reflexivity_asserts}
"""


def odrl_reflexivity(concepts: list[str]) -> str:
    """Generate reflexivity asserts for a list of concepts."""
    lines = [f"(assert (leq {c} {c}))" for c in concepts]
    return "\n".join(lines)


