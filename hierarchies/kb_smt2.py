ISO_CONCEPTS = [
    "europe",  # shared root (same constant as GEO — UF sort allows this)
    "dE", "fR", "pL", "iT", "eS",  # Germany, France, Poland, Italy, Spain
    "aT", "bE", "nL", "sE",        # Austria, Belgium, Netherlands, Sweden
]

ISO_LEQ = [
    ("dE", "europe"), ("fR", "europe"), ("pL", "europe"),
    ("iT", "europe"), ("eS", "europe"), ("aT", "europe"),
    ("bE", "europe"), ("nL", "europe"), ("sE", "europe"),
]

# NO sibling disjointness in ISO 3166 — it's a flat code list.
# UNA ($distinct) is present but that only gives dE ≠ fR, NOT disj(dE,fR).


def iso_smt2_kb() -> str:
    lines = []
    lines.append("; ─── ISO 3166 concepts (EU27 subset, flat code list) ─────────────")
    for c in ISO_CONCEPTS:
        if c == "europe":
            continue  # shared with GEO — already declared
        lines.append(f"(declare-const {c} C)")
    lines.append("")
    lines.append("; UNA (code-level distinctness — NOT structural disjointness)")
    iso_non_europe = [c for c in ISO_CONCEPTS if c != "europe"]
    lines.append(f"(assert (distinct {' '.join(iso_non_europe)}))")
    # Also distinct from all GEO concepts (europe shared, rest distinct)
    lines.append("")
    lines.append("; leq edges (flat: each code ≤ europe)")
    for (x, y) in ISO_LEQ:
        lines.append(f"(assert (leq {x} {y}))")
    lines.append("")
    lines.append("; Reflexivity")
    for c in ISO_CONCEPTS:
        lines.append(f"(assert (leq {c} {c}))")
    lines.append("")
    lines.append("; NOTE: NO disjoint/2 axioms — flat code list has no structure")
    return "\n".join(lines)


