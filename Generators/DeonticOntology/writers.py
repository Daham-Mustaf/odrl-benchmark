"""
writers.py
==========
File writers for the FOIS 2026 deontic grounding benchmark.

Exported functions:
  write_fof_problem(p, out_dir)   -> Path
  write_smt2_problem(p, out_dir)  -> Path

Imported by: gen_foundation_problems.py

All headers generated by header.py (Header / AXHeader / SMTHeader).
"""

import sys
import textwrap
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

from gen_layer0_signature import generate_smt2 as _gen_smt2
from header import Header, SMTHeader
from axiom_data import (
    FOF_AXIOM_DICT,
    SMT2_AXIOMS,
    SMT2_APPENDIX_SORTS,
    FOF_APPENDIX_DECLS,
)

VERSION       = "1.6"
SMT2_PREAMBLE = _gen_smt2()

_TTL_HEADER_MAX_LINES = 5

# ============================================================================
# HELPERS
# ============================================================================

def _get_axiom(key: str, problem_id: str) -> str:
    if key not in FOF_AXIOM_DICT:
        raise KeyError(
            f"Problem {problem_id}: axiom key '{key}' not in FOF_AXIOM_DICT.\n"
            f"Available: {sorted(FOF_AXIOM_DICT)}"
        )
    return FOF_AXIOM_DICT[key]


def _ttl_snippet(ttl: str, prefix: str) -> list[str]:
    all_lines = ttl.strip().splitlines()
    shown     = all_lines[:_TTL_HEADER_MAX_LINES]
    result    = [f"{prefix}{line}" for line in shown]
    remaining = len(all_lines) - _TTL_HEADER_MAX_LINES
    if remaining > 0:
        result.append(f"{prefix}... ({remaining} more lines — see Policies/ file)")
    return result


def _strip_percent(line: str) -> str:
    if line.startswith("% "):
        return line[2:]
    if line.startswith("%"):
        return line[1:]
    return line


def _description_lines(p: dict, prefix: str) -> list[str]:
    return [
        f"{prefix}{_strip_percent(line)}"
        for line in textwrap.dedent(p["description"]).strip().splitlines()
    ]


def _fof_body(p: dict) -> str:
    conj  = p.get("fof_conjecture")
    parts = [
        *[_get_axiom(ax, p["id"]) for ax in p.get("fof_axioms", [])],
        FOF_APPENDIX_DECLS,
        p["fof_extra_decls"],
    ]
    if conj is not None:
        parts += [
            "fof(conjecture, conjecture,",
            f"    ( {conj} )).",
        ]
    return "\n".join(parts)

# ============================================================================
# FOF WRITER
# ============================================================================

def write_fof_problem(p: dict, out_dir: Path) -> Path:
    subdir = out_dir / p["subdir"]
    subdir.mkdir(parents=True, exist_ok=True)
    path = subdir / f"{p['id']}-1.p"

    body    = _fof_body(p)
    english = "\n".join(_description_lines(p, ""))

    ttl_comment = ""
    if p.get("ttl"):
        ttl_lines = _ttl_snippet(p["ttl"], "")
        ttl_comment = (
            "Policy source: Policies/" + p["id"] + "-policy.ttl\n"
            + "\n".join(ttl_lines)
        )

    header = Header(
        file     = f"{p['id']}-1.p",
        domain   = "foundational",
        title    = p["name"],
        version  = VERSION,
        english  = english,
        status   = p["status_fof"],
        refs     = ["fois2026"],
        comments = (
            "Foundational ontology tier. FOIS 2026 benchmark.\n"
            "Requires Axioms/GRND000-0.ax (Layer 0) and\n"
            "inline Layer 1 axiom subset (fof_axioms key).\n"
            "FOF inlines per-problem subsets only to avoid Vampire timeouts.\n"
            "SMT-LIB embeds the full axiom set (Z3 handles it). Asymmetry intentional.\n"
            + ttl_comment
        ),
        fof_text = body,
    ).render()

    content = "\n".join([
        header,
        "",
        "% Layer 0: Signature (sorts, rfr/decl, position disjointness)",
        "include('Axioms/GRND000-0.ax').",
        "",
        "% Layer 1: Problem-specific axioms (subset of Ax5.1-5.11, A1-A3, B1-B3)",
        *[_get_axiom(ax, p["id"]) for ax in p.get("fof_axioms", [])],
        "",
        FOF_APPENDIX_DECLS,
        "%--------------------------------------------------------------------------",
        "% Ground instance (gamma)",
        "%--------------------------------------------------------------------------",
        p["fof_extra_decls"],
    ])

    conj = p.get("fof_conjecture")
    if conj is not None:
        content += "\n".join([
            "",
            "%--------------------------------------------------------------------------",
            "% Conjecture",
            "%--------------------------------------------------------------------------",
            "fof(conjecture, conjecture,",
            f"    ( {conj} )).",
        ])

    path.write_text(content, encoding="utf-8")
    return path

# ============================================================================
# SMT-LIB WRITER
# ============================================================================

def write_smt2_problem(p: dict, out_dir: Path) -> Path:
    subdir = out_dir / p["subdir"]
    subdir.mkdir(parents=True, exist_ok=True)
    path = subdir / f"{p['id']}-1.smt2"

    conj        = p.get("smt2_conjecture")
    skip_axioms = p.get("skip_smt2_axioms", False)

    desc = "\n".join(_description_lines(p, ""))
    ttl_comment = ""
    if p.get("ttl"):
        ttl_comment = (
            "\nPolicy source: Policies/" + p["id"] + "-policy.ttl\n"
            + "\n".join(_ttl_snippet(p["ttl"], ""))
        )

    smt_header = SMTHeader(
        file     = f"{p['id']}-1.smt2",
        domain   = "foundational",
        title    = p["name"],
        version  = VERSION,
        refs     = ["fois2026"],
        comments = (
            desc
            + "\nFoundational ontology tier. FOIS 2026 benchmark."
            + ttl_comment
        ).strip(),
        status   = p["status_smt"],
    ).render()

    body_lines = [
        "",
        "; === Layer 0 + Layer 1 preamble (embedded — SMT-LIB has no include) ===",
        "; === Source: Axioms/GRND000-0.smt2 ===",
        SMT2_PREAMBLE,
        "; === Appendix A.0 additional sorts/predicates ===",
        SMT2_APPENDIX_SORTS,
        "",
    ]

    if skip_axioms:
        body_lines += [
            "; === Layer 1: axioms omitted (skip_smt2_axioms=True) ===",
            "; === Problem is self-contained in smt2_extra_decls. ===",
            "",
        ]
    else:
        body_lines += [
            f"; === Layer 1: ALL {len(SMT2_AXIOMS)} axioms embedded ===",
            "; === Z3 handles full set; FOF inlines subsets only (Vampire timeout avoidance) ===",
            "; === Authoritative source: Axioms/GRND-AX-1.smt2 ===",
            "",
        ]
        for name, formula in SMT2_AXIOMS:
            body_lines.append(f"; {name}")
            body_lines.append(formula)
            body_lines.append("")

    body_lines += [
        "; === Ground instance (gamma) ===",
        p["smt2_extra_decls"],
    ]

    if conj is not None:
        body_lines += [
            "; === Negated conjecture ===",
            conj,
            "",
        ]

    body_lines.append("(check-sat)")

    path.write_text(smt_header + "\n" + "\n".join(body_lines), encoding="utf-8")
    return path