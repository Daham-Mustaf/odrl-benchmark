#!/bin/bash
# clean_for_geoff.sh — Prepare clean repo for Geoff Sutcliffe
# Run with: bash clean_for_geoff.sh
# DRY RUN first: bash clean_for_geoff.sh --dry-run

set -euo pipefail
DRY_RUN="${1:-}"

do_rm() {
    if [[ "$DRY_RUN" == "--dry-run" ]]; then
        echo "  REMOVE: $1"
    else
        rm -rf "$1"
    fi
}

echo "=== Cleaning repo for Geoff ==="
echo ""

# ─── 1. Remove legacy generators (superseded by gen_spatial_suite.py) ────
echo "--- Legacy generators ---"
for f in gen_tptp_ext.py gen_tptp_or_xone.py generate_scaled.py \
         generate_smtlib_ext.py generate_smtlib.py main.py run.py \
         evaluate_provers.py visualize_results.py gen_purpose_suite.py; do
    [[ -f "$f" ]] && do_rm "$f"
done

# ─── 2. Remove all legacy problem directories (not generated by current suite) ────
echo "--- Legacy problem directories ---"
for d in Problems/ODRL/KBGrounding/AdvDeep \
         Problems/ODRL/KBGrounding/Adversarial \
         Problems/ODRL/KBGrounding/AlignAdv \
         Problems/ODRL/KBGrounding/Alignment \
         Problems/ODRL/KBGrounding/CompDeep \
         Problems/ODRL/KBGrounding/CrossDataspace \
         Problems/ODRL/KBGrounding/HasPart \
         Problems/ODRL/KBGrounding/IsAllOf \
         Problems/ODRL/KBGrounding/IsAnyOf \
         Problems/ODRL/KBGrounding/IsNoneOf \
         Problems/ODRL/KBGrounding/Language \
         Problems/ODRL/KBGrounding/LogicalOr \
         Problems/ODRL/KBGrounding/LogicalXone \
         Problems/ODRL/KBGrounding/Neq \
         Problems/ODRL/KBGrounding/Nominal \
         Problems/ODRL/KBGrounding/OperatorPairs \
         Problems/ODRL/KBGrounding/Purpose \
         Problems/ODRL/KBGrounding/Runtime \
         Problems/ODRL/KBGrounding/Scaled \
         Problems/ODRL/killer-benchmark \
         Problems/ODRL/Golden; do
    [[ -d "$d" ]] && do_rm "$d"
done

# ─── 3. Remove legacy axiom files (keep only current 3 L0 + 1 L1 + 1 L2) ────
echo "--- Legacy axiom files ---"
for f in Problems/ODRL/Axioms/Layer0-DomainKB/CHN000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/CHN001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DIA000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DPV001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DPV002-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DPV003-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DPV004-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/GDPR001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/GEO001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/GEO002-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/GEO003-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/ISO3166-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/LNG000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/LNG001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/LNG002-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/MINI000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/MINI001-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/NMS000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/NOM000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/SNG000-0.ax \
         Problems/ODRL/Axioms/Layer0-DomainKB/DPV-README.md \
         Problems/ODRL/Axioms/Layer0-DomainKB/LNG-README.md \
         Problems/ODRL/Axioms/Layer0-DomainKB/LOGICAL-README.md \
         Problems/ODRL/Axioms/Alignment; do
    [[ -e "$f" ]] && do_rm "$f"
done

# ─── 4. Remove entire SMT directory (TPTP-only for Geoff) ────
echo "--- SMT-LIB (not for TPTP submission) ---"
[[ -d "SMT" ]] && do_rm "SMT"
[[ -d "Problems/ODRL/KBGrounding/SMT-LIB" ]] && do_rm "Problems/ODRL/KBGrounding/SMT-LIB"

# ─── 5. Remove legacy Solutions (stale proofs from old encodings) ────
echo "--- Legacy solutions ---"
[[ -d "Solutions" ]] && do_rm "Solutions"

# ─── 6. Remove legacy docs (keep only what's current) ────
echo "--- Legacy docs ---"
for f in docs/Alignment-validation-suite.md \
         docs/ANALYSISMODES-README.md \
         docs/benchmark-upgrade-plan.md \
         docs/framework-overview.md \
         docs/kiler \
         docs/layer.md \
         docs/ODRL-scenario.md \
         docs/RQ-paper-b.md \
         docs/semantic-inventory.md \
         docs/TPTP-ENCODING-README.md; do
    [[ -e "$f" ]] && do_rm "$f"
done

# ─── 7. Remove old results (keep only latest) ────
echo "--- Old results ---"
for f in results/benchmark_prover_20260215_*.csv \
         results/benchmark_purpose_*.csv \
         results/cactus.csv results/category_stats.csv \
         results/difficulty.csv results/evaluation.json \
         results/plots results/prover_table.tex \
         results/summary.csv results/syntax_stats.csv; do
    # Use glob-safe approach
    for match in $f; do
        [[ -e "$match" ]] && do_rm "$match"
    done
done

# ─── 8. Remove Isabelle temp files ────
echo "--- Isabelle temp files ---"
[[ -f "Isabelle/#Test.thy#" ]] && do_rm "Isabelle/#Test.thy#"

# ─── 9. Remove scripts dir (old runner) ────
echo "--- Old scripts ---"
[[ -d "scripts" ]] && do_rm "scripts"

# ─── 10. Remove Layer2 if unused ────
echo "--- Layer2 (check if still used) ---"
if ! grep -rq "GROUND000" Problems/ODRL/KBGrounding/Spatial/ 2>/dev/null; then
    do_rm "Problems/ODRL/Axioms/Layer2-Grounding"
    echo "  (not referenced by any current problem)"
fi

echo ""
echo "=== After cleaning, structure should be: ==="
echo ""
cat << 'TREE'
.
├── data/
│   └── dpv/dpv-owl.ttl              # Source ontology for DPV
├── gen_layer0_kb.py                  # L0 generator (OWL → TPTP, DAG-safe)
├── gen_layer0_geo.py                 # L0 generator (UN M49 hardcoded)
├── gen_layer0_lang.py                # L0 generator (BCP47 hardcoded)
├── gen_spatial_suite.py              # Problem generator (48 problems)
├── Isabelle/                         # Mechanical verification
│   ├── ODRL_Grounding.thy
│   ├── multi_parent_inconsistency.thy
│   └── ...
├── Problems/ODRL/
│   ├── Axioms/
│   │   ├── Layer0-DomainKB/
│   │   │   ├── GEO000-0.ax          # 430 axioms (tree, sibling-disj, no UNA)
│   │   │   ├── DPV000-0.ax          # 474 axioms (DAG-safe, no UNA)
│   │   │   └── LANG000-0.ax         # 445 axioms (forest, sibling-disj, no UNA)
│   │   └── Layer1-ODRLCore/
│   │       └── ODRL000-0.ax         # 33 axioms (KB properties + denotation rules)
│   └── KBGrounding/
│       └── Spatial/
│           ├── ODRL010-1.p .. ODRL069-1.p  # 48 benchmark problems
├── results/
│   └── benchmark_prover_20260216_154529.csv  # Latest: 46/48
├── docs/
│   └── PUSH_CHECKLIST.md
├── README.md
├── pyproject.toml
└── uv.lock
TREE

echo ""
if [[ "$DRY_RUN" == "--dry-run" ]]; then
    echo "DRY RUN complete. Run without --dry-run to actually delete."
else
    echo "Cleanup complete. Run 'tree' to verify."
fi
